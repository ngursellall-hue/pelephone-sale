/**
 * Generates vercel.json rewrites and js/campaign-routes.js from campaigns.json.
 * Run: npm run build  (also runs on Vercel deploy)
 */
'use strict';

var fs = require('fs');
var path = require('path');

var ROOT = path.join(__dirname, '..');
var campaignsPath = path.join(ROOT, 'campaigns.json');
var campaigns = JSON.parse(fs.readFileSync(campaignsPath, 'utf8'));

function phoneToTel(phone) {
  return String(phone).replace(/\D/g, '');
}

var routes = {};
var rewrites = [];

campaigns.forEach(function (c) {
  if (!c.slug || !c.phone || !c.manualCampaign) {
    throw new Error('Each campaign needs slug, phone, manualCampaign: ' + JSON.stringify(c));
  }
  var pagePath = '/' + c.slug + '/';
  routes[pagePath] = {
    phoneDisplay: c.phone,
    phoneTel: phoneToTel(c.phone),
    manualCampaign: String(c.manualCampaign),
    pagePath: pagePath
  };
  rewrites.push({ source: '/' + c.slug, destination: '/index.html' });
  rewrites.push({ source: '/' + c.slug + '/', destination: '/index.html' });
});

var routesJs =
  '/* AUTO-GENERATED by scripts/build-campaigns.js — edit campaigns.json and run npm run build */\n' +
  'window.PELEPHONE_CAMPAIGN_ROUTES = ' +
  JSON.stringify(routes, null, 2) +
  ';\n';

fs.writeFileSync(path.join(ROOT, 'js', 'campaign-routes.js'), routesJs, 'utf8');

var vercel = {
  buildCommand: 'npm run build',
  outputDirectory: '.',
  rewrites: rewrites
};

fs.writeFileSync(path.join(ROOT, 'vercel.json'), JSON.stringify(vercel, null, 2) + '\n', 'utf8');

/* Local static servers (Live Server, etc.) do not read vercel.json — copy index per slug */
var indexHtml = path.join(ROOT, 'index.html');
if (!fs.existsSync(indexHtml)) {
  throw new Error('Missing index.html at project root');
}
var indexSource = fs.readFileSync(indexHtml, 'utf8');

campaigns.forEach(function (c) {
  var dir = path.join(ROOT, c.slug);
  fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(path.join(dir, 'index.html'), indexSource, 'utf8');
});

console.log(
  '[build-campaigns] ' +
    campaigns.length +
    ' campaign(s) → js/campaign-routes.js, vercel.json, */index.html'
);
